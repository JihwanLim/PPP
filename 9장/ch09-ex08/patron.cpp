//--------------------------------------------------------------------------------------------------------------

Patron::Patron(string name)
	: name{ name }, late_fee{ 0 }
{
	string s;
	for (int i = 0; i < 15; ++i)
		s += '0' + (rand() % 10);
	id = s;
}

//--------------------------------------------------------------------------------------------------------------

bool hasLateFee(const Patron& p)
{
	return (p.getLateFee() > 0 ? true : false);
}

